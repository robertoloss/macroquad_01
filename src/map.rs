use::macroquad::prelude::Vec2;

#[derive(Debug)]
pub struct Tile {
    pub position: Vec2, 
    pub size: Vec2,
}

impl Tile {
    pub fn new(pos_x: usize, pos_y:usize) -> Self {
        let size : f32 = 40.0;
        Tile {
            position: Vec2 { 
                x: (pos_x as f32) * size,
                y: (pos_y as f32) * size
            },
            size:  Vec2 {
                x: size,
                y: size
            }
        }
    }
}

